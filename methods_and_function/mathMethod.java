package methods_and_function;

public class mathMethod {
    public static void main(String[] args) {
        int a=10;
        int b=20;
       

        System.out.println("Min of two Number : "+Math.min(a, b));
        System.out.println("Max of two Number : "+(int)Math.max(a, b));
        System.out.println("Square root : "+Math.sqrt(a));
        System.out.println("Power of two Number : "+Math.pow(2,5));
        System.out.println("Abs Number : "+Math.abs(-10));
    }
}
